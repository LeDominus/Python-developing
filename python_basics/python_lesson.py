massive_1 = [10, 11, 12, 13, 14, 15]
massive_2 = [16, 17, 18, 19, 20, 21]

# Создал функцию, которая находит среднее значение
def average(massives):
    average = sum(massives) / len(massives)
    return average

average_1 = average(massive_1)
print(average_1)

average_2 = average(massive_2)
print(average_2)


        
